//Program to swap two numbers using a third variable
#include <stdio.h>

int main() {
    int a, b, temp;

    printf("Enter two numbers:\n");
    scanf("%d %d", &a, &b);

    temp = a;   
    a = b;      
    b = temp;  

    printf("After swapping:\n");
    printf("a = %d\nb = %d\n", a, b);

    return 0;
}


